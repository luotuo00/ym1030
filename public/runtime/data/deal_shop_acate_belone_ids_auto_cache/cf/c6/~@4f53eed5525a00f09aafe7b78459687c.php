	<span style="font-family:微软雅黑, 'Microsoft YaHei';font-size:14px;"><strong>信任和沟通，差异化做互联网金融</strong></span>
</p>
<p style="background-color:#ffffff;text-indent:28px;color:#999999;">
	<span style="font-family:微软雅黑, 'Microsoft YaHei';font-size:14px;">融其道线下营业部的业务并不做太大改动，仍然采取线上交易的模式，门店的职能更多是增设沟通渠道，教会更多人使用互联网理财。这个做法不失为挖掘潜在客户的得力方式，同时也可成为寻找优质债权的管道，为投资人提供线下的直接窗口。不仅如此，通过实体店的形象塑造与展示，也能增加融其道的“高度和广度”。</span>
</p>
<p style="background-color:#ffffff;text-indent:28px;color:#999999;">
	<span style="font-family:微软雅黑, 'Microsoft YaHei';font-size:14px;">如同淘宝购物，买家要看卖家的信用和服务一样，所有互联网金融的业务也需要得到客户的信任和认可。传统金融企业互联网化，是互联网高速发展的大势所趋，而互联网金融业务扩张线下，则是因为在沟通渠道上的天然不足，解决这一软肋势在必行。</span>
</p>
<p style="background-color:#ffffff;text-indent:28px;color:#999999;">
	<span style="font-family:微软雅黑, 'Microsoft YaHei';font-size:14px;">正视这一行业痛点，是所有互联网金融企业的责任和使命，作为一家创新型互联网金融公司，融其道自然善守其位。</span>
</p>
