	<span style="color:#888888;">一直以</span><span style="color:#888888;">来，跑路、非法集资、庞氏骗局等问题一直抹黑着P2P行业。从去年年底开始，央视包括《焦点访谈》、《新闻1+1》在内的多档节目声援并肯定了P2P行业，中国人民大学金融与证券研究所吴晓求在节目中更是直接指出，现在出了事情的（公司）都是披了马甲的互联网金融，或者是假的互联网金融。都是假借互联网的平台从事非法融资活动、非法集资或者资金池等，都是违法行为。同时，行业诸多平台也在为P2P正名，点融网更是日前在发起了以“真的P2P、真的不跑路”为主题的营销活动。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">去年年底，征求意见稿发布之后，包括中央监管层、地方监管部门都对非法集资进行了排查和打击，合法合规成为P2P行业今年发展的关键词，而除了不做资金池、不期限错配、不自融等保持平台中介性质的硬性要求，选择符合条件的银行业金融机作为资金存管机构成为P2P行业的隐形门槛。无独有偶，</span><span style="color:#007aaa;">在去年7月18日央行发布的《关于促进互联网金融健康发展的指导意见》中也提到了进行银行资金存管。</span><span style="color:#888888;">可见，资金存管的重要性毋庸置疑。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<strong><span style="color:#007aaa;">P2P资金存管的几种模式</span></strong>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">目前，P2P平台的资金存管方式包括4种：</span><span style="color:#007aaa;">传统的“自存管”、第三方支付公司的联合存管、平台保留自主权的银行存管、银行主导的存管模式。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">传统的“自存管”可以说没有存管概念，就是让客户将资金直接打入公司开设的账户中，或者公司高管的个人账户。这类平台实际上存在自融、跑路的可能性就很大。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#007aaa;">第三方支付公司的联合存管是在银行与互联网金融平台之间，加入了第三方支付公司。</span><span style="color:#888888;">第三方支付公司将一些互联网金融平台打包在一起，并与银行谈存管条件。互联网金融平台的资金，存管于第三方支付公司，平台并不触碰资金。而第三方支付公司将无数个这样的互联网金融公司打包在一起，在银行设立备付金账户，并将打包的互联网金融平台的资金存放在该账户中，与银行形成存管。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">平台保留自主权的银行存管是在第二种模式的基础上，省去了第三方支付公司的环节。也就是说，</span><span style="color:#007aaa;">互联网金融平台在银行开设虚拟账户，这个虚拟账户与投资者是一一对应的关系。</span><span style="color:#888888;">一旦有交易出现，互联网金融平台就根据交易情况，向银行发出交易指令，银行则按照平台的指令完成交易。这种模式平台仍保留一定对资金运用的自主权，银行更多依据平台发出的指令进行资金划拨。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">银行主导的存管模式这种模式中，银行的作用至关重要。在投资者注册投资平台账户的同时，银行就已经在平台上设立了一一对应的账户。而当投资者投资或充值时，页面将自动从平台跳转至银行网站，而用户的交易密码则由银行掌管。用户每操作一步，银行都会及时将信息回馈给客户让其进行确认。</span><span style="color:#007aaa;">而用户也可在银行网站上，查询到自己在平台上的详细流水，做到点对点的操作。无论是用户投资，还是还款，账户都一一对应，保证平台无法接触到任何资金。</span>
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="line-height:1.75em;color:#888888;"><br />
</span>
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="line-height:1.75em;color:#888888;">目前来看，第一种“自存管”模式是监管坚决打击的，而其他几种模式都符合最终资金在银行进行存管的要求，目前来看均在监管底线之上。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<strong><span style="color:#007aaa;">银行存管之路任重而道远</span></strong>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">即便有三种模式供平台选择，但是截至目前，真正接入银行资金存管的平台数量寥寥。相关数据显示，在新规出台5个月后，与银行进行资金存管签约的平台只有100多家，而真正实施的则只有两位数。P2P行业实行银行资金存管任重而道远。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">民生银行作为较早推出网络借贷资金存管系统的商业银行，目前已经对接了积木盒子、人人贷、首金网三家平台，属于银行强存管，需要银行和平台进行大量的人员投入和技术对接，积木盒子整个对接过程长大9个月，人人贷更是长达1年时间。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">宜人贷和广发银行的合作则属于平台保留自主权的银行存管。宜人贷在广发银行设立了三类账户：第一类是P2P平台的服务费账户，是平台撮合交易成功后，根据借款人和出借人约定收取一部分服务费到该服务费账户，成为平台的自有资金。第二类是P2P交易资金托管账户，其中包含了借款人账户和出借人账户，借款人和出借人的账户资金由银行做全流程托管。第三类是风险金账户，按照交易规模的一定比例从服务费里提取风险金，这部分也是平台的自有资金。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#007aaa;">今年3月份，恒昌旗下恒易融与恒丰银行、上海汇付数据服务有限公司（汇付天下）开展的资金存管合作，则属于“第三方支付+银行”&nbsp;联合存管的模式，恒易融平台将实现客户资金和平台资金的完全隔离，用户的账户信息和资金流向都将受到恒丰银行的监督。</span><span style="color:#888888;">恒丰银行为恒昌公司、客户提供专户资金存管服务，包括专户开立（包括恒昌利通资金存管专户及风险准备金专户、汇付客户备付金专户）、相关资金清算及业务咨询等。上海汇付作为第三方支付公司，将为恒昌公司定制账户系统，并提供账户系统的外包运营服务，包括专属账户开户、客户信息管理等。同时，上海汇付为恒昌公司提供国内各商业银行跨行支付和资金结算服务。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<strong><span style="color:#007aaa;">资金存管是监管核心要义</span></strong>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">虽然监管没有明确提出P2P行业准入门槛，但是真正实行资金存管，不亚于注册资金、实缴资金的要求，这一征求意见稿中的隐形门槛也逐渐浮出水面，让业界了解到监管的高明和良苦用心。</span>
</p>
<p style="background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<br />
</p>
<p style="text-align:justify;background-color:#ffffff;text-indent:2em;color:#3e3e3e;">
	<span style="color:#888888;">实际上，国内金融业无论保险、证券、基金等实行的分业监管，但是监管的核心就是监控资金流向，所有从业单位都必须选择有资质的银行进行客户资金存管，实现自有资金和客户资金的绝对隔离。可以说，P2P行业与传统金融也在资金存管上一脉相承，也是P2P行业合规化的必经之路和标准配置。</span>
