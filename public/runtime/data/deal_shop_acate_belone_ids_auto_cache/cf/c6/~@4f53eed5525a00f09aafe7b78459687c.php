<p style="background-color:#ffffff;text-indent:28px;color:#999999;">
	<span style="font-family:微软雅黑, sans-serif;font-size:14px;">经历2014年的“野蛮”生长后，2015年互联网金融将迎来监管元年已是业内业外共识。中国支付清算协会副秘书长王素珍日前在第二届互联网金融全球峰会上表示，“2014年互联网金融监管留下的悬念，在2015年会得到明确答案，2015年将成为真正的互联网金融监管元年。”</span>
